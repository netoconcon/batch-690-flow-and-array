# STRING

"String".class # => string
"1".class # => string

# VARIABLE

age = 25

puts "I am #{age} years old"

# =>  "I am 25 years old"

# INTEGER AND FLOAT

12.class # => integer
12.0.class # => float

11 / 4 #=> 2
11.0 / 4 # => 2.75

# ARRAY

[].class # => array

# RANGES

(1..50).class # => range
(1..3).to_a # => [1, 2, 3]

# VOCABULARY

age = 20

# we assign the value 20 to the variable age

age = nil

# we initialized a var age
# but did not assign a value

age = 25

# we re-assigned a value to variable age

# INCREMENTATION

age = age + 1
age += 1 # same thing as age = age + 1

